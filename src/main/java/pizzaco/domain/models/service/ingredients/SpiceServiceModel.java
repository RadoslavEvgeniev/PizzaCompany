package pizzaco.domain.models.service.ingredients;

public class SpiceServiceModel {

    private String id;
    private String name;

    public SpiceServiceModel() {
    }

    public String getId() {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
